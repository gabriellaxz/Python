
a = float(input('Entre com o primeiro valor: '))
b = float(input('Entre com o segundo valor: '))
soma = a + b
subtracao = a - b
multiplicacao = a * b
divisao = a / b
resto = a % b
print('Soma: {soma}. \nSubtracao: {subtracao}.'
      '\nMultiplicacao: {multiplicacao}'
       '\nDivisao: {divisao}'
      '\nResto: {resto}' .format(soma=soma,
                                  subtracao=subtracao ,
                                  resto=resto ,
                                  multiplicacao=multiplicacao ,
                                  divisao=divisao))















